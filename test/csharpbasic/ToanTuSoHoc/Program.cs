{
    class Program
    {
        static void Main(string[] args)
        {
            int i = 5, j = 5;

            Console.WriteLine(i++); // Sử dụng giá trị của i để in ra rồi mới tăng i;
            Console.WriteLine(++j); // Tăng j rồi mới in  giá trị của  j ra màn hình;


            Console.ReadKey();

        }
    }
}
